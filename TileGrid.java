	/** Constructor
	  */
	public TileGrid(){		//contructor creates always the same map following the pattern of the binaryMap
		for(int i = 0; i<map.length; i++){
			for(int j=0 ; j<map[0].length; j++){
				if (binaryMap[i][j]==0){
					map[i][j] = new Ground(200+i*20, 20+j*20, 20, 20); 
				} else if(binaryMap[i][j]==1){
					map[i][j] = new Wall(200+i*20, 20+j*20, 20, 20); 
				} else if(binaryMap[i][j]==2){
					map[i][j] = new WinningTile(200+i*20, 20+j*20, 20, 20);
				}
			}
		}
	}
	
	
	/** displays the map
	  */
	public void draw(Graphics g){
		for(int i = 0; i<map.length; i++){
			for(int j = 0; j<map[i].length; j++){
				if(!(map[i][j] instanceof WinningTile)){//so that the WinningTiles are drawn last and their contour is entirely visible 
					map[i][j].paint(g); //ground and wall tiles have different paint methods
				}
			}
		}
		for(int i = 14; i<16; i++){
			for(int j = 14; j<16; j++){
				map[i][j].paint(g); 
			}
		}
	}
